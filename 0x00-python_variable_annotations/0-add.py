#!/usr/bin/env python3
"""
This module contains a type-annotated function to perform addition of two floats.

Description:
    The module defines a function `add` that takes two float arguments and returns their sum as a float.

Functions:
    - add(a: float, b: float) -> float: Returns the sum of two float values.
"""

def add(a: float, b: float) -> float:
    """
    Returns the sum of two float numbers.

    Args:
        a (float): The first float number.
        b (float): The second float number.

    Returns:
        float: The sum of `a` and `b`.
    """
    return a + b

